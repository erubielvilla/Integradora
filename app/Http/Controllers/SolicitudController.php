     */
    public function store(Request $request)
    {
        $Solicitud = new Solicitud;
            $Solicitud->Nombre_libro=$request->Nombre_libro;
            $Solicitud->Editorial=$request->Editorial;
            $Solicitud->Genero=$request->Genero;
            $Solicitud->Autor=$request->Autor;
            $Solicitud->Año_edicion=$request->Año_edicion;
            $Solicitud->Volumenes=$request->Volumenes;
            $Solicitud->ISBN=$request->ISBN;


        $Solicitud->save();

    return redirect()->route('Solicitud.index');
    }

